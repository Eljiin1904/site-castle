export function getLevelInfo(level: number) {
  const info = infos.find((x) => level >= x.start && level <= x.end);

  if (!info) {
    throw new Error("Level out of range");
  }

  return info;
}

export function findLevelInfo(
  predicate: (x: (typeof infos)[number]) => boolean,
) {
  const info = infos.find(predicate);

  if (!info) {
    throw new Error("Reward level info not found.");
  }

  return info;
}

